#include "main.h"
#include "lemlib/api.hpp"


#include "config.h"
pros::Motor left_front_motor (BLAHAJ_LF, BLAHAJ_GEAR_SET);
pros::Motor left_back_motor  (BLAHAJ_LB, BLAHAJ_GEAR_SET);
pros::Motor right_back_motor (BLAHAJ_RB, BLAHAJ_GEAR_SET);
pros::Motor right_front_motor(BLAHAJ_RF, BLAHAJ_GEAR_SET);
pros::Imu inertial_sensor(BLAHAJ_INERT);
pros::adi::DigitalOut piston (BLAHAJ_NEUMATIC_PORT);

pros::MotorGroup left_side_motors  ({BLAHAJ_LF,BLAHAJ_LB},BLAHAJ_GEAR_SET);
pros::MotorGroup right_side_motors ({BLAHAJ_RF,BLAHAJ_RB},BLAHAJ_GEAR_SET);


lemlib::Drivetrain drivetrain(&left_side_motors, // left motor group
                              &right_side_motors, // right motor group
                              BLAHAJ_TRACK, // 10 inch track width
                              BLAHAJ_WHEELTYPE, // using new 4" omnis
                              360, // drivetrain rpm is 360
                              2 // horizontal drift is 2 (for now)
);

lemlib::OdomSensors sensors(nullptr, // vertical tracking wheel 1, set to null
                            nullptr, // vertical tracking wheel 2, set to nullptr as we are using IMEs
                            nullptr, // horizontal tracking wheel 1
                            nullptr, // horizontal tracking wheel 2, set to nullptr as we don't have a second one
                            &inertial_sensor // inertial sensor
);

// lateral PID controller
lemlib::ControllerSettings lateral_controller(10, // proportional gain (kP)
                                              0, // integral gain (kI)
                                              3, // derivative gain (kD)
                                              3, // anti windup
                                              1, // small error range, in inches
                                              100, // small error range timeout, in milliseconds
                                              3, // large error range, in inches
                                              500, // large error range timeout, in milliseconds
                                              20 // maximum acceleration (slew)
);

lemlib::ControllerSettings angular_controller(2, // proportional gain (kP)
                                              0, // integral gain (kI)
                                              14, // derivative gain (kD)
                                              0, // anti windup
                                              0, // small error range, in inches
                                              0, // small error range timeout, in milliseconds
                                              0, // large error range, in inches
                                              0, // large error range timeout, in milliseconds
                                              0 // maximum acceleration (slew)
);

// create the chassis
lemlib::Chassis chassis(drivetrain, // drivetrain settings
                        lateral_controller, // lateral PID settings
                        angular_controller, // angular PID settings
                        sensors // odometry sensors
);

void screen() {
    // loop forever
    while (true) {
        pros::lcd::print(0, "Blahaj Bot - 30405 - %s",VERSION);
        lemlib::Pose pose = chassis.getPose(); // get the current position of the robot
        pros::lcd::print(1, "x: %f", pose.x); // print the x position
        pros::lcd::print(2, "y: %f", pose.y); // print the y position
        pros::lcd::print(3, "heading: %f", pose.theta ); // print the heading
        double average_temp = ((
            ( left_front_motor.get_temperature())+
              (left_back_motor.get_temperature())+
             (right_back_motor.get_temperature())+
            (right_front_motor.get_temperature())
            )/4);
        pros::lcd::print(4, "Average Temperature: %lf*C", average_temp); // print the heading
        //pros::lcd::set_background_color();
        pros::delay(10);
    }
}

void initialize() {
	pros::lcd::initialize();
    chassis.calibrate(); //calibrate sensors in robot
    pros::Task screenTask(screen);
}


void disabled() {}

void competition_initialize() {}

void autonomous() {
    //chassis.follow(BLAHAJ_PATH,(60+30)*1000,3);

    //ANGULAR CALIBRATION STEPS
    chassis.setPose(0, 0, 0);
    chassis.turnToHeading(90, 100000);
    //chassis.
}

void opcontrol() {
	pros::Controller master(pros::E_CONTROLLER_MASTER);
	

	while (true) {
		int left =  -master.get_analog(ANALOG_LEFT_Y);
		int right = -master.get_analog(ANALOG_RIGHT_Y);

        chassis.tank(left,right);

        if (master.get_digital(pros::E_CONTROLLER_DIGITAL_DOWN) == 1){
            autonomous();
        }

        pros::lcd::print(5, "Input: %d %d", left,right);
        pros::delay(2);
	}
}
